public class YesJava45 {
    public static void main(String[] args) {
        char m = 32;

        switch (m) {
            case 32:
                System.out.println("32");
                break;
            case 64:
                System.out.println("64");
                break;
            case 128:
                System.out.println("128");
                break;
        }
    }
}
