public class Inventar {
    private int invSize;

    public int getInvSize() {
        return invSize;
    }

    public void setInvSize(int invSize) {
        this.invSize = invSize;
    }

    @Override
    public String toString() {
        return "Inventar{" +
                "invSize=" + invSize +
                '}';
    }
}
